const getRecentSelectEmojis = () => {
    let recentSelectEmojis = [];

    try {
        recentSelectEmojis = JSON.parse(localStorage.getItem('recentSelectEmojis')) || [];
    } catch (err) {}

    return recentSelectEmojis.reverse().slice(0, 20);
};

const EmojiList = (props) => {
    const [ activedNav, setNav ] = useState(emojisNav[0]);

    const handleSecect = (e) => {
        const emoji = e.target.innerText;

        if (props.onSelect) {
            props.onSelect(emoji);
        }
    };

    const handleClick = (activedNav) => {
        setNav(activedNav);
        document.querySelector(`.${activedNav}`).scrollIntoView();
    };

    return (
        <div className="emoji-list">
            <header>
                {emojisNav.map(v => (<i key={v} title={v} onClick={handleClick.bind(null, v)} className={`iconfont icon-${v} ${activedNav === v ? 'actived' : ''}`}></i>))}
            </header>
            <main>
                <section>
                    <h1 className={emojisNav[0]}>Recents</h1>
                    <div>
                        {getRecentSelectEmojis().map((emoji, i) => <i onClick={handleSecect} key={i}>{emoji}</i>)}
                    </div>
                </section>
                {emojis.map(({ items, title }, i) => (
                    <section key={i}>
                        <h1 className={emojisNav[i + 1]}><i className={`iconfont icon-${emojisNav[i + 1]}`}></i>{title}</h1>
                        <div>
                            {items.split(' ').map((emoji, i) => <i onClick={handleSecect} key={i}>{emoji}</i>)}
                        </div>
                    </section>
                ))}
                <section>
                    <h1 className={emojisNav[emojisNav.length - 1]}>Customize</h1>
                    <CustomizeEmojiList />
                </section>
            </main>
        </div>
    );
}

EmojiList.storeEmojiRecents = (emoji) => {
    const recentSelectEmojis = getRecentSelectEmojis();

    localStorage.setItem('recentSelectEmojis', JSON.stringify(recentSelectEmojis.concat([...new Set(emoji)])));
}

export default EmojiList;
